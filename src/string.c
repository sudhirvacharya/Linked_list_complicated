int countSetBits(unsigned int n) {
    // Split 32-bit number into 4 bytes and sum precomputed counts
    return table[n & 0xFF] +
           table[(n >> 8) & 0xFF] +
           table[(n >> 16) & 0xFF] +
           table[(n >> 24) & 0xFF];
}

